    for r in 0..ROUNDS_F + ROUNDS_P {
        state = ark(state, &POSEIDON_C, r * T);
        if r < ROUNDS_F / 2 || r >= ROUNDS_F / 2 + ROUNDS_P {
            state = state.map(|a| sigma(a));
        } else {
            state[0] = sigma(state[0]);
        }
        state = mix(state, &POSEIDON_M);
    }

const T: usize = 5;

fn sigma(inp: Fp) -> Fp {
    let inp2 = inp * inp;
    let inp4 = inp2 * inp2;
    inp4 * inp
}

fn ark(mut inp: [Fp; 5], c: &[Fp], r: usize) -> [Fp; 5] {
    for i in 0..T {
        inp[i] += c[i + r];
    }
    inp
}

fn mix(inp: [Fp; T], m: &Vec<Vec<Fp>>) -> [Fp; T] {
    let mut out = [Fp::ZERO; T];
    for i in 0..T {
        for j in 0..T {
            out[i] += m[j][i] * inp[j];
        }
    }
    out
}

